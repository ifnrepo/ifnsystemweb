<script>
    // validasi
    $("#simpancustomer").click(function() {
        if ($("#kode_customer").val() == '') {
            pesan('Kode harus di isi !', 'error');
            return;
        }
        if ($("#nama_customer").val() == '') {
            pesan('Nama Customer harus di isi !', 'error');
            return;
        }
        $.ajax({
            dataType: "json",
            type: "POST",
            url: base_url + 'customer/simpancustomer',
            data: {
                kode_customer: $("#kode_customer").val(),
                nama_customer: $("#nama_customer").val(),
                exdo: $("#exdo").val(),
                port: $("#port").val(),
                country: $("#country").val(),
                alamat: $("#alamat").val(),
                desa: $("#desa").val(),
                kecamatan: $("#kecamatan").val(),
                kab_kota: $("#kab_kota").val(),
                propinsi: $("#propinsi").val(),
                kodepos: $("#kodepos").val(),
                npwp: $("#npwp").val(),
                telp: $("#telp").val(),
                email: $("#email").val(),
                kontak: $("#kontak").val(),
                keterangan: $("#keterangan").val(),
                buycode: $("#buycode").val(),
                inscode: $("#incode").val(),
                jcode1: $("#jcode1").val(),
                jcode2: $("#jcode2").val(),
                benua: $("#benua").val(),
                region: $("#region").val(),
                cust_id: $("#cust_id").val(),

            },
            success: function(data) {
                window.location.reload();

            },
            error: function(xhr, ajaxOptions, thrownError) {
                console.log(xhr.status);
                console.log(thrownError);
            }
        })
    })
</script>